public class Title5 {
        public static void main(String[] args){
            for(int k=1;k<=9;k++){
                for(int j=1;j<=k;j++){
                    System.out.print(j+"*"+k+"="+k*j+'\t');
                }
                System.out.println();
            }
        }

    }