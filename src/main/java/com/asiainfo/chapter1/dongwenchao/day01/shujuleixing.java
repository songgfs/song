public class shujuleixing {
    public static void main(String[] args)
    {
        //变量声明的三种格式
        boolean flag; //声明一个boolean类型的变量
        int a = 4; //声明一个int类型的变量并赋值
        int b;
        b=a;
        System.out.println(b);
        flag = true;  //给已经声明过的变量赋值
        // 变量名命名通常的规则是(当然不是必须的)：首字母小写，多个单词之间首字母大写
        byte b1 = 102;

        short s = 9;

        long g = 9999L;

        char c = '百';

        float f = 3.141F;

        double d = 8.56743d;

    }
}
