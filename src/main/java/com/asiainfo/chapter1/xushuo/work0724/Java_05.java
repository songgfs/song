public class Java_05 {
    public static void main(String[] args) {
        int a=2;
        switch(a) {
            case 1:
                System.out.println("a=1");
                break;
            case 2:
                System.out.println("a=2");
                break;
            case 3:
                System.out.println("a=3");
                break;
            default:
                System.out.println("默认输出结果");
        }
        System.out.println("switch case 条件控制语句");
    }
}
